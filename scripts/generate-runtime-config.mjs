function readJson(filePath) {
  try {
    return JSON.parse(fs.readFileSync(filePath, "utf8"));
  } catch (error) {
    throw new Error(`Unable to read deployed addresses JSON at ${filePath}: ${error.message}`);
  }
}

function writeJson(filePath, value) {
  writeText(filePath, `${JSON.stringify(value, null, 2)}\n`);
}

function writeText(filePath, value) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, value, "utf8");
}

function resolveContracts(value, filePath) {
  const govCoreAddress = firstAddress(value, [
    ["contracts", "govCoreAddress"],
    ["contracts", "govCore"],
    ["IsoniaProtocolV01Module#GovCore"],
    ["GovCore"],
    ["govCoreAddress"],
    ["govCore"],
  ]);
  const govProposalsAddress = firstAddress(value, [
    ["contracts", "govProposalsAddress"],
    ["contracts", "govProposals"],
    ["IsoniaProtocolV01Module#GovProposals"],
    ["GovProposals"],
    ["govProposalsAddress"],
    ["govProposals"],
  ]);
  const demoTargetAddress = firstAddress(value, [
    ["contracts", "demoTargetAddress"],
    ["contracts", "demoTarget"],
    ["IsoniaProtocolV01Module#DemoTarget"],
    ["DemoTarget"],
    ["demoTargetAddress"],
    ["demoTarget"],
  ]);

  const missing = [];
  if (!govCoreAddress) missing.push("govCoreAddress");
  if (!govProposalsAddress) missing.push("govProposalsAddress");
  if (!demoTargetAddress) missing.push("demoTargetAddress");
  if (missing.length > 0) {
    const keys = Object.keys(value).sort().join(", ") || "none";
    throw new Error(
      `Unable to resolve ${missing.join(", ")} from ${filePath}. ` +
        `Known Ignition keys include IsoniaProtocolV01Module#GovCore, ` +
        `IsoniaProtocolV01Module#GovProposals, and IsoniaProtocolV01Module#DemoTarget. ` +
        `Top-level keys found: ${keys}`,
    );
  }

  return {
    govCoreAddress,
    govProposalsAddress,
    demoTargetAddress,
  };
}

function firstAddress(value, paths) {
  for (const candidatePath of paths) {
    const candidate = getPath(value, candidatePath);
    if (isAddress(candidate)) {
      return candidate;
    }
  }
  return undefined;
}

function getPath(value, keys) {
  let current = value;
  for (const key of keys) {
    if (!current || typeof current !== "object" || !(key in current)) {
      return undefined;
    }
    current = current[key];
  }
  return current;
}

function isAddress(value) {
  return typeof value === "string" && /^0x[a-fA-F0-9]{40}$/.test(value);
}

function readString(name, fallback) {
  const value = process.env[name];
  return value === undefined || value === "" ? fallback : value;
}

function readPackageVersion(name, fallback) {
  return readString(name, fallback).replace(/^v/, "");
}

function toTag(version) {
  return `v${version}`;
}

function readInteger(name, fallback) {
  const raw = readString(name, String(fallback));
  const value = Number(raw);
  if (!Number.isSafeInteger(value)) {
    throw new Error(`Invalid integer environment variable ${name}: ${raw}`);
  }
  return value;
}

function readBoolean(name, fallback) {
  const raw = process.env[name];
  if (raw === undefined || raw === "") {
    return fallback;
  }
  if (raw === "true") return true;
  if (raw === "false") return false;
  throw new Error(`Invalid boolean environment variable ${name}: ${raw}`);
}

function readWalletConnectionMode(name) {
  const value = readString(name, "injected-only");
  if (["auto", "appkit", "injected-only"].includes(value)) {
    return value;
  }
  throw new Error(
    `Invalid wallet connection mode ${name}: ${value}. ` +
      "Expected auto, appkit, or injected-only.",
  );
}

function envLine(name, value) {
  return `${name}=${shellQuote(value)}`;
}

function shellQuote(value) {
  return `'${String(value).replaceAll("'", "'\\''")}'`;
}
